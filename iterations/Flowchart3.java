package com.qa.iteration;

public class Flowchart3 {
	public void myValue() { 
		for (int j =1; j <11; j ++) {
			for (int i = 1;  i < 11; i++) {
				System.out.println(i);
		}
			}
	}
			
		


	public void myValue2(int number) { 
		for (int j =1; j <= number; j ++) {
			for (int i = 1;  i <= j; i++) {
				System.out.println(j);
		}

		}
		
	}
}